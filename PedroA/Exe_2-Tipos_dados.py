#Inicio
#String -> palavras 'utiliza-se aspas simples ou duplas'
computador = 'DELL'
print('a variável computador é do tipo', type (computador))
#integer(inteiro) -> números inteiros 1; 2; 3; 4; 5;...100;
idade = 53
print('a variável idade é do tipo:', type (idade))
#Float(flutuante) -> números fracionados exemplos: 1,3 e 2,3;
altura = 1.8
print('A variável altura é do tipo:', type(altura))
#Bool(Booleano) -> True(Verdadeiro) ou False(Falso)
fumante = True
print('A variável fumante é do tipo:', type(fumante))
